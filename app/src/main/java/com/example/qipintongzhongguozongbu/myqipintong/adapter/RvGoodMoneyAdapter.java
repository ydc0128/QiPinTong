public class RvGoodMoneyAdapter extends BaseQuickAdapter<String, BaseViewHolder> {

    public RvGoodMoneyAdapter(int layoutResId, List<String> data) {
        super(layoutResId, data);
    }

    @Override
    protected void convert(BaseViewHolder helper, String item) {
        Glide.with(mContext).load(item).placeholder(R.mipmap.loding).error(R.mipmap.lodingerror).into((ImageView) helper.getView(R.id.siv_face_money_icon));
    }
}